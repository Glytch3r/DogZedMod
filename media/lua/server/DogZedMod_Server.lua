require "DogZedMod_Util"
--server
if isClient() then return; end

local Commands = {};
Commands.DogZedMod = {};

Commands.DogZedMod.isDogZed = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('DogZed', 'isDogZed', {id = playerId, isDogZed = args.isDogZed,  zedID = args.zedID})
end


Commands.DogZedMod.setAnimSpeed = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('DogZed', 'setAnimSpeed', {id = playerId, AnimSpeed = args.AnimSpeed,  zedID = args.zedID})
end

Commands.DogZedMod.KnockDown = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('DogZed', 'KnockDown', {id = playerId, zedID = args.zedID})
end

Commands.DogZedMod.isDogPl = function(player, args)
    local playerId = player:dgetOnlineID();
    sendServerCommand('DogZed', 'isDogPl', {id = playerId, isDogPl =  args.isDogPl})
end

Commands.DogZedMod.Injury = function(player, args)
    local playerId = player:getOnlineID();
    sendServerCommand('DogZed', 'Injury', {id = playerId, targID = args.targID})
end



Commands.DogZedMod.doDespawn = function(player, args)
    local zed = DogZedMod.findzedID(tonumber(args.zedID))
    if zed then
        zed:removeFromWorld();
        zed:removeFromSquare();
    end
end


Commands.DogZedMod.doSpawn = function(player, args)
    local x, y, z, count, fit, fChance, isDown = args.x,  args.y,  args.z, args.count, args.fit, args.fChance, args.isDown
    local zed = addZombiesInOutfit(round(x), round(y), round(z), 1, tostring(fit), tonumber(fChance), isDown, false, isDown, isDown, 1.0)
    if zed then
        if isDown then
            if not zed:isOnFloor() then
                zed:knockDown(true)
            end
        end
    end
end

Events.OnClientCommand.Add(function(module, command, player, args)
	if Commands[module] and Commands[module][command] then
	    Commands[module][command](player, args)
	end
end)


