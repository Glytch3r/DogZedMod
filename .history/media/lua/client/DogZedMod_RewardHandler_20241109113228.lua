DogZedMod = DogZedMod or {}


function DogZedMod.SpawnRewards(sq)
	local itemList = DogZedMod.parseItems()
	if sq then
		for _, item in ipairs(itemList) do
			local roll = DogZedMod.getRewardRoll()
			if roll then
				sq:AddWorldInventoryItem(item, ZombRand(0, 3), ZombRand(0, 3), 0)
			end
		end
	end
end

function DogZedMod.getRewardRoll()
	local chance = SandboxVars.DogZedMod.DropRate or 20
	if chance == 0 then return false end
	if chance == 100 then return true end
	return DogZedMod.doRoll(chance)
end

function DogZedMod.getDeathRewards()
	return SandboxVars.DogZedMod.DeathRewards or 'Base.HuntingKnife;Base.RippedSheetsDirty'
end

function DogZedMod.parseItems()
    local tab = {}
	local lootStr = DogZedMod.getDeathRewards()
    for item in string.gmatch(lootStr, "([^;]+)") do
        table.insert(tab, item)
    end
    return tab
end

