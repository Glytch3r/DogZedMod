DogZedMod = DogZedMod or {}

-----------------------               ---------------------------


function DogZedMod.getPanicRate()
	return SandboxVars.DogZedMod.PanicRate or  0.4
end
function DogZedMod.getPanicDistance()
	return SandboxVars.DogZedMod.PanicDistance or 4
end
function DogZedMod.isPanicDisarm()
	return SandboxVars.DogZedMod.PanicDisarm or true
end



function DogZedMod.PanicHandler(zed)
	local pl = getPlayer(); if not pl then return end
	local range = DogZedMod.getPanicDistance()
	if DogZedMod.isDogZed(zed) and DogZedMod.checkDist(pl, zed) <= range then
		local panic = pl:getStats():getPanic()
		local panicRate = DogZedMod.getPanicRate()
		local newPanic = math.min(100, panic + panicRate)
		pl:getStats():setPanic(newPanic)
	end
end
Events.OnZombieUpdate.Remove(DogZedMod.PanicHandler)
Events.OnZombieUpdate.Add(DogZedMod.PanicHandler)


-----------------------            ---------------------------

function DogZedMod.ShadowCloneHandler(char1, char2)
	local targ = nil
	if instanceof(char1, "IsoZombie") and  DogZedMod.isDogZed(char1)  then
		targ = char1
	elseif instanceof(char2, "IsoZombie") and  DogZedMod.isDogZed(char2) then
		targ = char2
	end


	if targ then
		if DogZedMod.isShadowClone(targ) then
			DogZedMod.doDespawn(targ)
		elseif DogZedMod.isRadiatedDog(targ) then
			local sq = targ:getSquare()
			DogZedMod.setRadiateSq(sq)
		end
	end
end
Events.OnCharacterCollide.Remove(DogZedMod.ShadowCloneHandler)
Events.OnCharacterCollide.Add(DogZedMod.ShadowCloneHandler)


function DogZedMod.ShadowCloneHandler2(zed, ...)
	if instanceof(zed, "IsoZombie") and DogZedMod.isShadowClone(zed) then
		DogZedMod.doDespawn(zed)
	end
end
Events.OnObjectCollide.Add(DogZedMod.ShadowCloneHandler2)




